package com.pojo;

public class Addition6 {
	Addition6(int num1,int num2)
	{
		System.out.println("Addition is "+num1+num2);
	}
	
	Addition6(int num1,int num2,int num3)
	{
		System.out.println("Addition is "+num1+num2+num3);
	}
	
	Addition6(int num1,int num2,int num3,int num4,int num5)
	{
		System.out.println("Addition is "+num1+num2+num3+num4+num5);
	}
	
	Addition6(int num1,int num2,int num3,int num4,int num5,int num6)
	{
		System.out.println("Addition is "+num1+num2+num3+num4+num5+num6);
	}
	

}
